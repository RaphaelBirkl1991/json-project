abstract class UserI {
  void changeEmail();
  void changePassword();
  void sendVerificationEmail();
}

class User implements UserI {
  @override
  void changeEmail() {
    // Ändere E-Mail
    print("E-Mail geändert...");
  }

  @override
  void changePassword() {
    // Ändere Passwort
    print("Passwort geändert...");
  }

  @override
  void sendVerificationEmail() {
    // Sende E-Mail
    print("E-Mail gesendet...");
  }
}

void main() {
  User user = User();

  user.changeEmail();
  user.changePassword();
  user.sendVerificationEmail();
}
